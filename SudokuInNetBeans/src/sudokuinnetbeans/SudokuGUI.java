    private void jButtonGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGenerateActionPerformed
        int gridSize = Integer.parseInt(jTextFieldInputSize.getText());
    jPanelGrid.removeAll();
    jPanelGrid.setLayout(new GridLayout(gridSize, gridSize));

    grid = new JTextField[gridSize][gridSize];  // Use the class-level variable now

    for (int i = 0; i < gridSize; i++) {
        for (int j = 0; j < gridSize; j++) {
            grid[i][j] = new JTextField(2);
            grid[i][j].setHorizontalAlignment(JTextField.CENTER);
            jPanelGrid.add(grid[i][j]);
        }
    }

    jPanelGrid.revalidate();
    jPanelGrid.repaint();

    // Create the puzzle using your external logic
    solver = new SudokuSolved(gridSize, grid);
        
    }//GEN-LAST:event_jButtonGenerateActionPerformed

