private void runBanner(){
    Timer t;
        t = new Timer(100, new ActionListener(){
            int count = 0;
            String bannerText = jLabelBanner.getText();
            @Override
            public void actionPerformed(ActionEvent e){
                count = (count + 1) % bannerText.length();
                jLabelBanner.setText(bannerText.substring(count)+bannerText.substring(0, count));
            }
        });
    t.start();
}

