    private void SubmitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitBtnActionPerformed
        // TODO add your handling code here:
        String inputText = TextArea.getText();
        int length = inputText.length();
        lengthDisplay.setText("" + length);

        int lines = inputText.split("\n", -1).length;
        linesCount.setText("" + lines);

        int tabCount = inputText.split("\t", -1).length - 1;
        tabsCount.setText("" + tabCount);
        
        int spaceCount = inputText.split(" ").length - 1;
        spacesCount.setText("" + spaceCount);
        
        int wordCount;
        if (inputText.trim().isEmpty()) {
            wordCount = 0;
        } else {
            wordCount = inputText.split("\\s+").length;
        }
        wordsCount.setText("" + wordCount);

    }//GEN-LAST:event_SubmitBtnActionPerformed

    private void textCountButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_textCountButtonActionPerformed
        // TODO add your handling code here:
        String searchText = textCountInput.getText().toLowerCase();
        String fullText = TextArea.getText().toLowerCase();
        int count = 0;
        int index = 0;
        while ((index = fullText.indexOf(searchText, index)) != -1) {
            count++;
            index += searchText.length();
            textCountDisplay.setText("" + count);
        }
    }//GEN-LAST:event_textCountButtonActionPerformed

