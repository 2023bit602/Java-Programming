    private void characterCountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_characterCountActionPerformed
        // TODO add your handling code here:
        String selectedCharacter = characterChoice.getSelectedItem().toString();
        String inputText = TextArea.getText().toLowerCase();
        int charCount = 0;
        for (int i = 0; i < inputText.length(); i++) {
            if (inputText.charAt(i) == selectedCharacter.charAt(0)) {
                charCount++;
            }
        }
        characterCountDisplay.setText(""+charCount);
    }//GEN-LAST:event_characterCountActionPerformed

